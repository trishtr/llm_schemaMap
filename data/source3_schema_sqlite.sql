-- Source 3: Inventory Management System (UPPER_CASE naming convention) - SQLite3 Version
-- This represents an enterprise system with standardized naming

-- Customer master table
CREATE TABLE CUSTOMER_MASTER (
    CUST_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    CUST_FNAME TEXT NOT NULL,
    CUST_LNAME TEXT NOT NULL,
    CUST_EMAIL TEXT UNIQUE NOT NULL,
    CUST_PHONE TEXT,
    CUST_DOB TEXT,
    CUST_CREATE_DT TEXT DEFAULT CURRENT_TIMESTAMP,
    CUST_STATUS TEXT DEFAULT 'A',
    CUST_ORDER_CNT INTEGER DEFAULT 0,
    CUST_TOTAL_AMT REAL DEFAULT 0.00
);

-- Item master table
CREATE TABLE ITEM_MASTER (
    ITEM_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    ITEM_NAME TEXT NOT NULL,
    ITEM_DESC TEXT,
    ITEM_CAT TEXT,
    ITEM_PRICE REAL NOT NULL,
    ITEM_STOCK INTEGER DEFAULT 0,
    ITEM_SUPPLIER TEXT,
    ITEM_CREATE_DT TEXT DEFAULT CURRENT_TIMESTAMP,
    ITEM_UPDATE_DT TEXT DEFAULT CURRENT_TIMESTAMP
);

-- Purchase order table
CREATE TABLE PURCHASE_ORDER (
    PO_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    PO_CUST_ID INTEGER NOT NULL,
    PO_DATE TEXT DEFAULT CURRENT_TIMESTAMP,
    PO_STATUS TEXT DEFAULT 'PENDING',
    PO_TOTAL REAL NOT NULL,
    PO_SHIP_ADDR TEXT,
    PO_PAY_METHOD TEXT,
    FOREIGN KEY (PO_CUST_ID) REFERENCES CUSTOMER_MASTER(CUST_ID)
);

-- Purchase order detail table
CREATE TABLE PO_DETAIL (
    POD_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    POD_PO_ID INTEGER NOT NULL,
    POD_ITEM_ID INTEGER NOT NULL,
    POD_QTY INTEGER NOT NULL,
    POD_UNIT_PRICE REAL NOT NULL,
    POD_DISC_AMT REAL DEFAULT 0.00,
    FOREIGN KEY (POD_PO_ID) REFERENCES PURCHASE_ORDER(PO_ID),
    FOREIGN KEY (POD_ITEM_ID) REFERENCES ITEM_MASTER(ITEM_ID)
);

